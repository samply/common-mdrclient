
package de.samply.common.mdrclient.domain;

/**
 * Enumerator of data types as defined in the MDR.
 *
 * @author diogo
 */
public enum EnumDataType {

  /**
   * A date.
   */
  DATE,
  /**
   * A date and time.
   */
  DATETIME,
  /**
   * A time.
   */
  TIME,
  /**
   * An integer.
   */
  INTEGER,
  /**
   * A float.
   */
  FLOAT,
  /**
   * A string.
   */
  STRING,
  /**
   * A boolean.
   */
  BOOLEAN,
  /**
   * Contains a catalogue.
   */
  CATALOG;

}
